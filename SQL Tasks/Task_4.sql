--5th question
/*
Identify the maximum purchase amount generated by each salesperson for each 
order date by writing a SQL query. Sort the result set in ascending order by 
salesperson ID and order date.
*/
select salesman_id,order_date, max(purch_amt) from orders group by salesman_id, order_date
order by salesman_id,order_date

--6th question
/*
Create a SQL query that records the number of distinct orders and the maximum 
purchase amount for every client.
*/
select customer_id, count(distinct order_no),max(purch_amt) from orders group by customer_id
