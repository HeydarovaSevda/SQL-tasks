/*
Write a SQL query to find the number of orders booked for each day.
*/
select count(order_date) as number_of_orders, order_date from orders group by order_date

--3rd question
/*
Write a SQL query to show all the orders. Sort the result-set in ascending order 
by ord_no. Return all fields.*/
